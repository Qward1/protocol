"""Системные промпты для ASR / диаризации (OpenRouter)."""

from __future__ import annotations


def asr_system_prompt(language: str, diarization: bool) -> str:
    diar = (
        "Размечай говорящих метками «Спикер 1», «Спикер 2» и т.д., сохраняя их "
        "согласованность по всей записи.\n"
        "При каждой смене говорящего начинай НОВУЮ строку с собственным тайм-кодом "
        "и меткой говорящего — не объединяй реплики разных спикеров в одну строку.\n"
        if diarization
        else "Не пытайся различать говорящих.\n"
    )
    return (
        "Ты — система точной транскрибации аудио на русском языке.\n"
        f"Язык записи: {language}.\n"
        "Транскрибируй дословно (verbatim), без пересказа и без додумывания.\n"
        "Разбивай речь на короткие реплики/сегменты — одна реплика на строку.\n"
        "Каждый сегмент начинай с тайм-кода в формате [мм:сс] относительно начала фрагмента.\n"
        f"{diar}"
        "Если слышишь неразборчивую речь — помечай как [неразборчиво].\n"
        "Формат строки: [мм:сс] Спикер N: текст реплики\n"
        "Не добавляй комментариев, заголовков и пояснений вне транскрипта."
    )


ASR_USER_INSTRUCTION = (
    "Транскрибируй приложенный фрагмент аудио согласно правилам. "
    "Верни только строки транскрипта."
)
